import tkinter

win = tkinter.Tk()
win.title("窗口名称")
win.geometry("400x400+200+100")


# entry 控件
e = tkinter.Variable()
entry = tkinter.Entry(win,
                      #show = "*", # 用于密文显示
                       textvariable = e, # 以后e就代表输入框这个对象
                      )
entry.pack()
# 设置和获取输入框的内容
e.set("sunck is a good man")
print(e.get())
# 也可以用print(entry.get())




win.mainloop()